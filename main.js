const year = document.getElementById("year");
if (year) year.textContent = new Date().getFullYear();

const langEN = document.getElementById("langEN");
const langES = document.getElementById("langES");

function setActiveLangButtons(lang) {
  if (!langEN || !langES) return;
  langEN.classList.toggle("is-active", lang === "en");
  langES.classList.toggle("is-active", lang === "es");
}

function applyTranslations(lang) {
  const dict = i18n[lang] || i18n.en;

  document.documentElement.setAttribute("lang", lang);
  document.documentElement.dataset.lang = lang;

  // Text content
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (key && dict[key]) el.textContent = dict[key];
  });

  // Placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (key && dict[key]) el.setAttribute("placeholder", dict[key]);
  });

  // Update title for better UX
  document.title =
    lang === "es"
      ? "WebHall — Donde tu trabajo ocupa el centro del escenario"
      : "WebHall — Where Your Work Takes Center Stage";

  localStorage.setItem("webhall_lang", lang);
  setActiveLangButtons(lang);
}

langEN?.addEventListener("click", () => applyTranslations("en"));
langES?.addEventListener("click", () => applyTranslations("es"));

// Load saved language or infer from browser
const savedLang = localStorage.getItem("webhall_lang");
const browserPref = (navigator.language || "en").toLowerCase().startsWith("es") ? "es" : "en";
applyTranslations(savedLang || browserPref);

// Mobile menu toggle
const menuBtn = document.getElementById("menuBtn");
const mobileNav = document.getElementById("mobileNav");

menuBtn?.addEventListener("click", () => {
  const isOpen = menuBtn.getAttribute("aria-expanded") === "true";
  menuBtn.setAttribute("aria-expanded", String(!isOpen));
  mobileNav.hidden = isOpen;
});

// Close mobile menu when clicking a link
document.querySelectorAll("#mobileNav a").forEach((a) => {
  a.addEventListener("click", () => {
    if (!menuBtn || !mobileNav) return;
    menuBtn.setAttribute("aria-expanded", "false");
    mobileNav.hidden = true;
  });
});

